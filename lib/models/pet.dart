class Pet {
  String id;
  String name;
  String gender;
  String type;
  String breed;
  String picture;
  String age;
  String location;
  String ownerID;
  String addedDate;
  String description;

  Pet({
    required this.id,
    required this.name,
    required this.gender,
    required this.type,
    required this.breed,
    required this.picture,
    required this.age,
    required this.location,
    required this.ownerID,
    required this.addedDate,
    required this.description,
  });
}
